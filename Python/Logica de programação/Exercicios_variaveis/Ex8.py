"""8. Faça um código que receba 4
números e realize a soma deles e a
média entre eles. e mostre os
resultados.
"""

#Variaveis que recebem os valores
numero1 = int(input("Digite o Primeiro numero: "))
numero2 = int(input("Digite o Segundo numero: "))
numero3 = int(input("Digite o Terceiro numero: "))
numero4 = int(input("Digite o Quarto numero: "))

#Variavel responsavel por fazer o calculo
media = (numero1+numero2+numero3+numero4)/4

print(f"O Resultado da media dos 4 numeros e {media}")